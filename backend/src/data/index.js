// خرائط تحويل بين المفتاح البرمجي والاسم العربي
export const KEY_BY_ARABIC = {
  "عقد إيجار": "rental",
  "عقد تمويل": "financing",
  "عقد استثماري": "investment",
};

export function resolveContractKey(input) {
  if (!input) return null;
  if (SAMPLE_ANALYSES[input]) return input;      // مفتاح برمجي مباشر
  if (KEY_BY_ARABIC[input]) return KEY_BY_ARABIC[input]; // اسم عربي
  return null;
}
